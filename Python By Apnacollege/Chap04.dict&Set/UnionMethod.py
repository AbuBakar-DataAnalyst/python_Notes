# union (Al values) & intersection (Olny Same values) same's like in Math rule

set1 = { 3 , 5 , 7 , 4, 3 , 3 , 9 , 8 , "Abi"  }
set2 = { 9 , 6 , 1 , 0 , 2 , 3 , "Abi"}

print(set1.union(set2)) # In Set only sigle values are store , Repeative values not store

print(set1.intersection(set2)) # intersection